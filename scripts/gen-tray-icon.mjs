#!/usr/bin/env node
// Generates the menu-bar / tray icon as base64 PNGs baked into a TS source file,
// so we don't need to ship binary image assets or worry about packaging paths.
// A tray icon is tiny (16/32px) — a hand-rolled PNG encoder is simpler than a
// dependency. Draws an IDM-style download arrow (stem + head + baseline tray).
//
//   node scripts/gen-tray-icon.mjs   ->  src/main/trayIcon.ts
import { deflateSync } from 'zlib'
import { writeFileSync } from 'fs'
import { fileURLToPath } from 'url'
import { dirname, join } from 'path'

const __dirname = dirname(fileURLToPath(import.meta.url))

// --- minimal PNG encoder (RGBA, 8-bit) ---------------------------------------
const CRC_TABLE = (() => {
  const t = new Int32Array(256)
  for (let n = 0; n < 256; n++) {
    let c = n
    for (let k = 0; k < 8; k++) c = c & 1 ? 0xedb88320 ^ (c >>> 1) : c >>> 1
    t[n] = c
  }
  return t
})()

function crc32(buf) {
  let c = ~0
  for (let i = 0; i < buf.length; i++) c = CRC_TABLE[(c ^ buf[i]) & 0xff] ^ (c >>> 8)
  return ~c >>> 0
}

function chunk(type, data) {
  const len = Buffer.alloc(4)
  len.writeUInt32BE(data.length, 0)
  const typeBuf = Buffer.from(type, 'ascii')
  const body = Buffer.concat([typeBuf, data])
  const crc = Buffer.alloc(4)
  crc.writeUInt32BE(crc32(body), 0)
  return Buffer.concat([len, body, crc])
}

function encodePng(width, height, rgba) {
  const sig = Buffer.from([0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a])
  const ihdr = Buffer.alloc(13)
  ihdr.writeUInt32BE(width, 0)
  ihdr.writeUInt32BE(height, 4)
  ihdr[8] = 8 // bit depth
  ihdr[9] = 6 // color type RGBA
  // 10,11,12 = compression/filter/interlace = 0
  const stride = width * 4
  const raw = Buffer.alloc((stride + 1) * height)
  for (let y = 0; y < height; y++) {
    raw[y * (stride + 1)] = 0 // filter: none
    rgba.copy(raw, y * (stride + 1) + 1, y * stride, y * stride + stride)
  }
  const idat = deflateSync(raw, { level: 9 })
  return Buffer.concat([
    sig,
    chunk('IHDR', ihdr),
    chunk('IDAT', idat),
    chunk('IEND', Buffer.alloc(0))
  ])
}

// --- draw the download-arrow glyph ------------------------------------------
// Coordinates are defined on a 32-unit grid and scaled to the target size, with
// 4x supersampling for smooth edges.
function drawArrow(size, [r, g, b]) {
  const SS = 4
  const S = size * SS
  const buf = Buffer.alloc(S * S * 4)
  const u = S / 32 // one grid unit in supersampled pixels
  const cx = S / 2

  // Arrow geometry on the 32-grid.
  const stemW = 6 * u
  const stemTop = 4 * u
  const stemBot = 17 * u
  const headTop = 13 * u
  const headBot = 23 * u
  const headHalf = 10 * u
  const trayY = 25 * u
  const trayH = 3 * u
  const trayHalf = 11 * u

  const inStem = (x, y) =>
    x >= cx - stemW / 2 && x <= cx + stemW / 2 && y >= stemTop && y <= stemBot
  const inHead = (x, y) => {
    if (y < headTop || y > headBot) return false
    // Triangle narrowing to a point at headBot.
    const t = (headBot - y) / (headBot - headTop) // 0 at tip, 1 at base
    return Math.abs(x - cx) <= headHalf * t
  }
  const inTray = (x, y) =>
    y >= trayY && y <= trayY + trayH && Math.abs(x - cx) <= trayHalf

  for (let py = 0; py < size; py++) {
    for (let px = 0; px < size; px++) {
      let hits = 0
      for (let sy = 0; sy < SS; sy++) {
        for (let sx = 0; sx < SS; sx++) {
          const x = px * SS + sx + 0.5
          const y = py * SS + sy + 0.5
          if (inStem(x, y) || inHead(x, y) || inTray(x, y)) hits++
        }
      }
      const a = Math.round((hits / (SS * SS)) * 255)
      const o = (py * size + px) * 4
      buf[o] = r
      buf[o + 1] = g
      buf[o + 2] = b
      buf[o + 3] = a
    }
  }
  return encodePng(size, size, buf)
}

const b64 = (buf) => buf.toString('base64')

// macOS: template image must be black + alpha; the OS recolors it for light/dark
// menu bars automatically. Windows/Linux: use a light gray so it shows on dark
// trays and isn't invisible on light ones.
const macColor = [0, 0, 0]
const winColor = [220, 224, 230]

const out = `// AUTO-GENERATED by scripts/gen-tray-icon.mjs — do not edit by hand.
// Base64 PNGs for the menu-bar / tray icon (download arrow). macOS variants are
// black template images (the OS tints them); Windows variants are light gray.

export const TRAY_MAC_16 = '${b64(drawArrow(16, macColor))}'
export const TRAY_MAC_32 = '${b64(drawArrow(32, macColor))}'
export const TRAY_WIN_16 = '${b64(drawArrow(16, winColor))}'
export const TRAY_WIN_32 = '${b64(drawArrow(32, winColor))}'
`

const dest = join(__dirname, '..', 'src', 'main', 'trayIcon.ts')
writeFileSync(dest, out)
console.log('Wrote', dest)
